// Helper function to get translations
export const getTranslation = (
  language: 'en' | 'zh',
  section: 'common' | 'page' | 'component',
  key: string,
  subKey?: string
): Record<string, string> => {
  if (section === 'common') {
    return commonTranslations[language];
  } else if (section === 'page' && subKey) {
    return pageTranslations[key as keyof typeof pageTranslations]?.[language] || {};
  } else if (section === 'component' && subKey) {
    return componentTranslations[key as keyof typeof componentTranslations]?.[language] || {};
  }
  return {};
};
